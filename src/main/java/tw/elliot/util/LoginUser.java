package tw.elliot.util;

public class LoginUser {
	private String account;

	public LoginUser() {
		super();
	}

	public LoginUser(String account) {
		super();
		this.account = account;
	}

	public String getAccount() {
		return account;
	}

	public void setAccount(String account) {
		this.account = account;
	}

}
